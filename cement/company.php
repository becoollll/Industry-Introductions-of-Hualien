            <h3 id="0">水泥產業概觀</h3>
            <p>水泥是國家基礎工業之一，也是建築業與各項大型建設不可或缺的材料。</p>
            <p>水泥笨重、密度高，易受潮而硬化變質，故無法久存，不適合長途運輸，且故以內銷為主，屬於傳統內需導向型工業，一般國家水泥的自給率約為85%，其榮枯與否主要繫於國內公共工程與建築業的景氣狀況而定。不過，由於水泥業衍生的環保節能、生態保育問題甚多，故大多先進國家逐漸將水泥業轉移至開發中國家，於是亞洲成為國際水泥業的兵家必爭之地。</p>
            <p>再者，水泥業的技術障礙雖然不大，但需要投入龐大的資本、機器設備、採礦權、土地成本及運輸費用等，屬於資本密集產業，進入門檻甚高，形成典型的寡佔市場，箇中「玩家」不多，於是整體產業變動不大，相對穩定。</p>
            <p>台灣水泥產業國內兩大龍頭廠為台泥及亞泥，其在國內水泥市佔率已達8成，產業趨近於飽和，故價格變化相對不受供需所影響，反而是必須看政策的變化。</p>
            <p>近年來，由於政策與國際競爭的考量，國內廠商紛紛轉往中國發展。台泥、亞泥受惠中國去產能政策下，水泥業持續朝向健康供需的態勢發展，「大廠恆大」的優勢日益明顯。</p>
            <p>由於國內營建業習慣於農曆年前趕工，因此每年 11 月、12 月是水泥需求較大的旺季，7月、8月則適逢雨季、颱風季，水泥產品易滯銷，亦即水泥業的淡季。</p>
