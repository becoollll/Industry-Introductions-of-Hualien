            <h3 id="3">下游產業 - 混凝土、工程營建</h3>
            <p>當製作好水泥於磨成後，先貯存於水泥儲存庫中，再依顧客需求包裝出貨，水泥包裝可分為袋裝、太空包袋及散裝。</p>
            <p>另外，廠商也會將粉煤灰、煤渣、煤矸石、尾礦渣、化工渣或者天然砂、海塗泥等作為主要原料，用水泥做凝固劑，不經高溫煆燒而製成一種新型牆體材料，稱之為水泥磚；將水泥和砂、石子混合在一起，套入瓦片模型，即可製成水泥瓦。</p>
            <p>水泥若是再加上膠凝材料、骨料、水，按適當比例配置，即製成預拌混凝土，是一種經過時間硬化而成的人工石材，通常在設備完善的工廠中先拌合好，再運送至工址使用，其原料來源廣泛、成本低廉、製作方法簡單，而且硬度高、堅固耐用、可塑性強、適用於各種自然環境，是世界上使用量最大的人工建築材料。</p>
            <p>水泥產業國內兩大龍頭廠為台泥及亞泥，其在國內水泥市場的佔有率已達八成，產業近飽和，因此價格變化受產業供需影響較小，反受政策的影響較大。</p>
