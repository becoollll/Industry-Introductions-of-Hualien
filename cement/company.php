            <h3 id="1">上游產業 - 水泥原料</h3>
            <p>水泥業最上游的主要原料是石灰石，約佔 77%-82%，再來則是黏土、矽砂、鐵渣、爐渣等</p>
            <p>水泥生產中間必須經過很多過程，一般可分為生料磨製（生料磨系統）、鍛燒（旋窯系統）和水泥製成（水泥磨系統）三個工序，生料磨製即是石灰石和黏土等原料，進行磨機磨細之工序；鍛燒即把已加工之原料放入窯；燒完後再經過粉磨，就成為水泥。因此，窯燒水泥與煉油一樣，都必須燃燒煤炭，並耗費大量電力。</p>
            <p>水泥的成本結構依序分別為煤（40％）、石灰石（25％），電力（12%），機器設備的折舊與維修（10%），剩下的才是其他要素。占水泥業成本最高比重的是煤，而煤與石油又處於替代性的關係，故油價走高時，也會牽動煤的價格上升，成本提高，除非水泥業的下游需求旺盛，可提高售價，否則石油價格上漲將會使水泥毛利降低。</p>
            <p>2019 年，中美貿易戰的走向勢必影響油價，貿易保護主義政策的實施，將造成全球經濟放緩，並削弱對原物料及大宗商品的需求，也將導致原油需求量下滑，油價下跌。</p>
