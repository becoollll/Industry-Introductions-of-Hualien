            <h3 id="2">中游產業 - 水泥成品</h3>
            <p>水泥原料可分為生料、熟料。生料是由石灰質原料、黏土質原料及少量校正原料（有時還加入礦化劑、晶種等，立窯生產時還要加煤）按比例配合，磨到一定細度的的石灰粉；水泥熟料則是水泥製作過程中的一個中間產品，主要是石灰石和黏土等材料，經過1500度高溫加熱、鍛燒後，形成顆粒狀的小圓球。</p>
            <p>有些水泥為了不同用途會添加石膏。石膏的主要化學成分是硫酸鈣（CaSO4），主要是古代鹽湖或潟湖的沉積物，可用於水泥緩凝劑、石膏建築製品、模型製作、醫用食品添加劑、硫酸生產、紙張填料、油漆填料、骨折固定等，也能做為黑板用的粉筆。天然二水石膏又稱為生石膏，經過鍛燒、磨細可得 β 型半水石膏，即建築石膏，又稱熟石膏、灰泥。若鍛燒溫度為 190°C 可得模型石膏，其細度和白度均比建築石膏高。若將生石膏在 400～500°C 或高於 800°C 下鍛燒，即得地板石膏，其凝結、硬化較慢，但硬化後強度、耐磨性和耐水性均較普通建築石膏為好。</p>
